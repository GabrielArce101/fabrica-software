idade = float(input("digite a idade \n"))
if(idade>=0) and (idade <=2):
    print(f"a idade é de um bebe")
elif 3 <= idade <=11:
    print(f"a idade é de uma criança")

elif 12 <= idade <=21:
    print(f"idade é de um jovem")

elif 22<= idade <= 64:
    print(f"a idade é de um adulto")

elif 65<= idade <=100:
    print(f" a idade é de um idoso")

elif 101<= idade:
    print(f"a idade é de alguém muito idoso")




